    to_download = [d for d in FLUX_MAP[resolution][flux] if (d[0]>=start_year) and (d[0]<=end_year)]
    
    _saveloc = os.path.expanduser(saveloc)

    if not os.path.exists(_saveloc):
        os.makedirs(_saveloc) 
        
    for _file in to_download:
        print(f"outputing: {os.path.join(_saveloc, _file[1])}")
        data.save_to_folder(_file[-1], _saveloc)    
    
    
    
